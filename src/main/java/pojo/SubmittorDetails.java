package pojo;

public class SubmittorDetails {

    private int submittorid;
    private String submitterName;
    private String submitterType;


    public int getsubmittorid()
    {
        return submittorid;
    }

    public void setsubmittorid(int submittorid)
    {
        this.submittorid = submittorid;
    }

    public String getSubmitterName()
    {
        return submitterName;
    }

    public void setSubmitterName(String submitterName)
    {
        this.submitterName = submitterName;
    }

    public String getSubmitterType()
    {
        return submitterType;
    }
    public void setSubmitterType(String submitterType)
    {
        this.submitterType = submitterType;
    }

}
