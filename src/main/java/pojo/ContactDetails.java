package pojo;

public class ContactDetails {
    private int id;
    private int MobileNumber;

    public int getid() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMobileNumber() {
        return MobileNumber;
    }

    public void setMobileNumber(int MobileNumber) {
        this.MobileNumber = MobileNumber;
    }
}
