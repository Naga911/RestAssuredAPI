package pojo;

public class BuildingDetails {

    private int id;
    private int buildingId;
    private String applicationBuildingNickname;
    private int applicationBuildingHeight;
    private int applicationBuildingNumberOfFloors;
    private int applicationBuildingNumberofBasementFloors;
    private int applicationBuildingGroundFloorSize;
    private int applicationBuildingNumberOfStaircases;
    private int applicationBuildingNumberOfExits;
    private int applicationBuildingNumberOfLifts;
    private int applicationBuildingTotalArea;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBuildingId() {
        return buildingId;
    }

    public void setBuildingId(int BuildingId) {
        this.buildingId = BuildingId;
    }

    public String applicationBuildingNickname() {
        return applicationBuildingNickname;
    }

    public void setApplicationBuildingNickname(String BuildingNickname) {
        this.applicationBuildingNickname = BuildingNickname;
    }


    public int getApplicationBuildingHeight() {
        return applicationBuildingHeight;
    }

    public void setApplicationBuildingHeight(int height) {
        this.applicationBuildingHeight = height;
    }

    public int getApplicationBuildingNumberOfFloors() {
        return applicationBuildingNumberOfFloors;
    }

    public void setApplicationBuildingNumberOfFloors(int floors) {
        this.applicationBuildingNumberOfFloors = floors;
    }

    public int getApplicationBuildingNumberofBasementFloors() {
        return applicationBuildingNumberofBasementFloors;
    }

    public void setApplicationBuildingNumberofBasementFloors(int Bfloors) {
        this.applicationBuildingNumberofBasementFloors = Bfloors;
    }

    public int getapplicationBuildingGroundFloorSize() {
        return applicationBuildingGroundFloorSize;
    }

    public void setApplicationBuildingGroundFloorSize(int Gfloor) {
        this.applicationBuildingGroundFloorSize = Gfloor;
    }

    public int getapplicationBuildingNumberOfStaircases() {
        return applicationBuildingNumberOfStaircases;
    }

    public void setApplicationBuildingNumberOfStaircases(int Stairs) {
        this.applicationBuildingNumberOfStaircases = Stairs;
    }

    public int getApplicationBuildingNumberOfExits() {
        return applicationBuildingNumberOfExits;
    }

    public void setApplicationBuildingNumberOfExits(int Exists) {
        this.applicationBuildingNumberOfExits = Exists;
    }

    public int getApplicationBuildingNumberOfLifts() {
        return applicationBuildingNumberOfLifts;
    }

    public void setApplicationBuildingNumberOfLifts(int Lifts) {
        this.applicationBuildingNumberOfLifts = Lifts;
    }

    public int getApplicationBuildingTotalArea() {
        return applicationBuildingTotalArea;
    }

    public void setApplicationBuildingTotalArea(int Area) {
        this.applicationBuildingTotalArea = Area;
    }

}
